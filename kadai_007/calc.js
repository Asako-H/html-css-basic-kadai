// 1以上の整数を代入する
let num = 15;
// 変数numの値を出力する
console.log(num);

// 変数の値が3の倍数
if (num % 3 === 0) {
  console.log("3の倍数です");
}
// 変数の値が5の倍数
if (num % 5 === 0) {
  console.log("5の倍数です");
}
// 変数の値が3と5の倍数
if (num % 3 === 0 && num % 5 === 0) {
  console.log("3と5の倍数です");
}
